void printImage(int[,] inImage)
{
    for (int i = 0; i < inImage.GetLength(0); i++)
    {
        for (int j = 0; j < inImage.GetLength(1); j++)
        {
            if (inImage[i, j] == 0) Write($" ");
            else Write($"+");
        }
        WriteLine();
    }
}

void fillImage(int inRow, int inColumn)
{
    if (pic[inRow, inColumn] == 0)
    {
        pic[inRow, inColumn] = 1;
        fillImage(inRow - 1, inColumn);
        fillImage(inRow, inColumn - 1);
        fillImage(inRow + 1, inColumn);
        fillImage(inRow, inColumn + 1);
    }
}